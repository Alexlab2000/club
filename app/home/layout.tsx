/**
 * ФАЙЛ: app/home/layout.tsx
 * НАЗНАЧЕНИЕ: Обёртка для всех страниц раздела /home
 *
 * Layout — это компонент-обёртка. Всё что внутри /home/*
 * рендерится через этот layout.
 *
 * ИСПРАВЛЕНИЕ:
 * Раньше здесь была ДОПОЛНИТЕЛЬНАЯ проверка пользователя через
 * supabase.auth.getUser(). Это создавало 3 проверки на один запрос:
 *   1. middleware.ts — проверяет каждый запрос
 *   2. home/layout.tsx — проверяет при входе в /home
 *   3. home/page.tsx — проверяет при загрузке страницы
 *
 * Три проверки = три запроса к Supabase = медленная загрузка.
 * Плюс — состояние гонки: layout показывал "Проверка доступа..."
 * пока page.tsx уже рендерился.
 *
 * РЕШЕНИЕ: middleware.ts уже защищает /home (исправлено в middleware).
 * Layout просто рендерит children без лишних проверок.
 */

// Нет "use client" — это серверный компонент (по умолчанию в Next.js 14)
// Серверные компоненты не используют useState и useEffect.

export default function HomeLayout({
  children,
}: {
  children: React.ReactNode; // children — это вложенные страницы (/home/page.tsx и т.д.)
}) {
  // Middleware уже гарантировал что пользователь залогинен.
  // Просто рендерим дочерние страницы.
  return <>{children}</>;
}
